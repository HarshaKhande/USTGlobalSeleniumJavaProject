package USTGlobal.SeleniumJava;

public class ForEg {

	public static void main(String[] args) {

		// for loop can be used for the fixed iteration
		
		for (int i = 1; i <=10; i++) {
			
			System.out.println(i);
		}
		
		// descending order
		
		for (int i = 10; i >=1; i--) {
			
			System.out.println(i);
		}


	}

}
