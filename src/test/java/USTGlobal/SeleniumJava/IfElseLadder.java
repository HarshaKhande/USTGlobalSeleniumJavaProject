package USTGlobal.SeleniumJava;

public class IfElseLadder {

	public static void main(String[] args) {
		
		String branch = "MG road";
		
		if (branch.equals("BTM")) {
			
			System.out.println("BTM branch");
			
		} else if (branch.equals("HSR")) {
			
			System.out.println("HSR branch");
			
		} else if (branch.equals("Chickpet")) {
			
			System.out.println("Chickpet branch");
			
		} else {
			
			System.out.println("No branch found");
		}
		
		



	}

}
