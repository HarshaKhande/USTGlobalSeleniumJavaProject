package USTGlobal.SeleniumJava;

public class DataTypes {

	public static void main(String[] args) {
		
		
		// String data type - words 
		
		String name = "Selenium";
		
		System.out.println(name);
		
		// boolean 
		
		boolean b = true ;
		
		System.out.println(b);
		
		// char - single character - ' ' - 16 bit unicode character
		
		char c = 'A';
		
		System.out.println(c);
		
		// byte - 8 bit signed integer -128 to 127
		
		byte by = 127;
		
		System.out.println(by);	
		
		// short - 16 bit signed integer - -32768 to 32767
		
		short s = 32767;
			
		System.out.println(s);
			
		// int - 32 bit signed integer - -2147483648 to 2147483647
		
		int i = 2147483647;
		
		//	 long - 64 bit signed integer - -9223372036854775808 to 9223372036854775807
		
		long l = 9223372036854775807L;
			
		System.out.println(l);
		
		// float - 32 bit floating point - 1.4E-45 to 3.4028235E38
		
		float f = 3.4028235E38F;
		
		System.out.println(f);
		
				
		// double - 64 bit floating point - 4.9E-324 to 1.7976931348623157E308
		
		double d = 1.7976931348623157E308;
		
	    System.out.println(d);
		
		
			
					
				
				
			
			
		
		
			

	}

}
