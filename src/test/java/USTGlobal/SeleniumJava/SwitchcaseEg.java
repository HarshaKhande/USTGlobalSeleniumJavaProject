package USTGlobal.SeleniumJava;

public class SwitchcaseEg {

	public static void main(String[] args) {
		
		int num = 9;
		
		switch(num) {
		
		case 1: System.out.println("num is 1");
		// break will stop the execution of the program
		break;
		
		case 2: System.out.println("num is 2");
		break;
		
		case 3: System.out.println("num is 3");
		break;
		
		default: System.out.println("num is not 1,2,3");
		
		}

	}

}
